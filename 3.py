def neob(x, y, z, a=4, b=2):
    """
    Возвращает x + y + z * a / b
    :параметр x: целое число.
    :параметр y: целое число.
    :параметр z: целое число.
    :параметр a: целое число.
    :параметр b: целое число.
    :return: производит вычисление выражения x + y + z * a / b.
    """
    return x + y + z * a / b 

